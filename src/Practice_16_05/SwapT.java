package Practice_16_05;

public class SwapT {
    public static void main(String[] args) {
        int a=50;
        int b=90;
        int temp=0;
        System.out.println("a= "+a);
        System.out.println("b= "+b);
        temp=a;
        a=b;
        b=temp;
        System.out.println("a= "+a);
        System.out.println("b= "+b);
    }
}
